import math

# Natural logarithm of 5
log_5 = math.log(5)
print('Natural logarithm of 5 is', log_5)

# e raised to the power of 3
e_power_3 = math.exp(3)
print('e raised to the power of 3 is', e_power_3)

# Square root of 7
sqrt_7 = math.sqrt(7)
print('Square root of 7 is', sqrt_7)

# Sine of 90 degrees
sine_90_degrees = math.sin(math.radians(90))
print('Sine of 90 degrees is', sine_90_degrees)
