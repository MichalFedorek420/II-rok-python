numbers = [7, 5, 6, 3, 8, 2]

# Find the largest number
max_number = max(numbers)
print('The largest number among 7, 5, 6, 3, 8, 2 is', max_number)

# Binary representation of 304
binary_304 = bin(304)
print('Binary representation of 304 is', binary_304)

# Hexadecimal representation of 304
hex_304 = hex(304)
print('Hexadecimal representation of 304 is', hex_304)

# Unicode code of the € sign
euro_unicode = ord('€')
print('Unicode code of the € sign is', euro_unicode)

# Absolute value of -17
abs_value = abs(-17)
print('The absolute value of -17 is', abs_value)
